public class Temperatura {
   

        public static void main(String[] args) throws Exception {
            double C, K, F;
            C = 3.43;
            F = C * 1.8 + 32;
            K = C + 273.15;
    
            System.out.println("A temperatura em Fahrenheit é:" + F);
            System.out.println("A temperatura em Kelvin é: " + K);


            
    }
}
